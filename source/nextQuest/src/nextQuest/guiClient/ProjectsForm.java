private void b_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_saveActionPerformed
      //splitDate("11.04.2005");
    System.out.println();
    if(t_name.getText().length() < 3 || t_name.getText().length() > 40) {
                JOptionPane.showMessageDialog(null, "Length of a name must be between 3 and 40 characters...", "Input error", JOptionPane.ERROR_MESSAGE);
                return;
            } else if (t_priority.getText().length() < 1 || t_priority.getText().length() > 5) {
                JOptionPane.showMessageDialog(null, "Length of a priority must be between 1 and 5 numbers...", "Input error", JOptionPane.ERROR_MESSAGE);
                return;
            } else if(!isNumeric(t_priority.getText())){
              JOptionPane.showMessageDialog(null, "Priority must be number.", "Input error", JOptionPane.ERROR_MESSAGE);
                return;  
            }else if(ChLeader.getSelectedIndex()==-1){
              JOptionPane.showMessageDialog(null, "Create an Leader", "Input error", JOptionPane.ERROR_MESSAGE);
                return;  
            }
     if(projInf != null) {
         pm.editProject(projInf, t_name.getText(), usrInf2.get(ChLeader.getSelectedIndex()),Integer.valueOf(t_priority.getText()));
     }else{
        try {
            pm.createProject(t_name.getText(), usrInf2.get(ChLeader.getSelectedIndex()),Integer.valueOf(t_priority.getText()));
        } catch (nqException ex) {
            Logger.getLogger(ProjectsForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(ProjectsForm.class.getName()).log(Level.SEVERE, null, ex);
        }
     }
  dispose();
        
}//GEN-LAST:event_b_saveActionPerformed

