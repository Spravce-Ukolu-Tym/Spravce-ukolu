        private void initializeRow() throws RemoteException, nqException {
        UserInfo usrInf[] = uma.listAllUsers();
        for (UserInfo info : usrInf) {
                if(info.getPermissionLeader()){
                  usrInf2.add(info);  
                }
            }
            for (UserInfo info : usrInf2) {
                ChLeader.addItem(info.getName());
            }
    }
